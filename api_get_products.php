<?php
include 'db.php';

$sql = "SELECT Product_id, Product_name FROM Product_Info WHERE Stock > 10";
$result = $conn->query($sql);

$products = array();
if ($result->num_rows > 0) {
    while($row = $result->fetch_assoc()) {
        $products[] = $row;
    }
}

header('Content-Type: application/json');
echo json_encode($products);

